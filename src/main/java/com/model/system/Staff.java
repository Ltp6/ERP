package com.model.system;

import java.util.Date;

public class Staff {
    private String staId;

    private String staName;

    private String loginName;

    private String staTel;

    private Date staIntime;

    private String staSex;

    private String staStatus;

    private String staAddress;

    private String staEdu;

    private String staIdcard;

    public String getStaId() {
        return staId;
    }

    public void setStaId(String staId) {
        this.staId = staId;
    }

    public String getStaName() {
        return staName;
    }

    public void setStaName(String staName) {
        this.staName = staName;
    }

    public String getLoginName() {
        return loginName;
    }

    public void setLoginName(String loginName) {
        this.loginName = loginName;
    }

    public String getStaTel() {
        return staTel;
    }

    public void setStaTel(String staTel) {
        this.staTel = staTel;
    }

    public Date getStaIntime() {
        return staIntime;
    }

    public void setStaIntime(Date staIntime) {
        this.staIntime = staIntime;
    }

    public String getStaSex() {
        return staSex;
    }

    public void setStaSex(String staSex) {
        this.staSex = staSex;
    }

    public String getStaStatus() {
        return staStatus;
    }

    public void setStaStatus(String staStatus) {
        this.staStatus = staStatus;
    }

    public String getStaAddress() {
        return staAddress;
    }

    public void setStaAddress(String staAddress) {
        this.staAddress = staAddress;
    }

    public String getStaEdu() {
        return staEdu;
    }

    public void setStaEdu(String staEdu) {
        this.staEdu = staEdu;
    }

    public String getStaIdcard() {
        return staIdcard;
    }

    public void setStaIdcard(String staIdcard) {
        this.staIdcard = staIdcard;
    }
}